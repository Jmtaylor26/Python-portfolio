def filter_dogs_by_size(dogs, min_weight, max_weight, size_category):
    filtered_dogs = []
    for i in range(len(dogs)):
        if size_category == "tiny" and max_weight[i] <= 10:
            filtered_dogs.append(dogs[i])
        elif size_category == "small" and min_weight[i] >= 11 and min_weight[i] <= 25:
            filtered_dogs.append(dogs[i])
        elif size_category == "medium" and min_weight[i] >= 26 and min_weight[i] <= 60:
            filtered_dogs.append(dogs[i])
        elif size_category == "large" and min_weight[i] > 60:
            filtered_dogs.append(dogs[i])

    return filtered_dogs

def get_user_input_and_display():
    size_category = input("Enter the size category (tiny, small, medium, large): ").lower()
    filtered_dogs = filter_dogs_by_size(dogs, min_weight, max_weight, size_category)
    if len(filtered_dogs) > 0:
        print("\nDogs in the " + size_category + " size category:")
        for dog in filtered_dogs:
            print(dog)
        else:
            print(f"\nNo dogs found in the {size_category} size category.")

